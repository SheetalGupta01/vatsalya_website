                    <h4><b>The Problem or issues at hand:</b></h4>
                    <p>Children with borderline intellectual functioning and various learning, speech, visual and hearing difficulties are conservatively estimated to be more than 12% of the total child population. </p>
                    <p>Many children are at risk of experiencing emotional, academic and behavioral problems which hampers their overall growth. In addition to a child’s physical health and genetic makeup, various psycho-social factors such as parental neglect, discord in the family, physical abuse, ways of coping with stress, disciplining practices, parental control, parent-child relationship, loss or separation from a loved one, deprivation, discrimination, learning disability, psychiatric conditions etc. are known to influence the child’s mental health and overall functioning. </p>
                    <h4><b>The Need or the Solution </b></h4>
                    <p>Addressing such risk factors early in age can play a crucial role in preventing its adverse effects and enable children reach their emotional and intellectual potential.</p>  
                    <p>In several cases, emotional trauma or the inability to deal with emotions in adaptive ways are known to have either exacerbate the problems or caused hindrance in effectively coping with the problems.  It leads to interference with or reduced quality of social, academic, or occupational functioning. Psychological counseling aims to help people manage their difficulties and transform personal problems into their own personal growth.</p>
                    <p>Bearing in mind the pivotal nature of this project and the increasing number of people who are willing to take advantage of such facility, Vatsalya Trust needs to sustain and expand their current facilities to cover a larger number of families with children in need.<p>
